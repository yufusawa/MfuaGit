#!/bin/bash
# Скрипт, который рекурсивно добавляет в каждый .md файл фразу
# Запрашиваем подтверждение один раз в начале
read -p "Добавить фразу во все .md файлы (кроме уже содержащих её)? (y/N) " global_answer
if [[ ! "$global_answer" =~ ^[Yy]$ ]]; then
    echo "Операция отменена."
    exit 0
fi

# Счётчики для статистики
updated=0
already_has=0

# Рекурсивный обход всех .md файлов с использованием process substitution,
# чтобы переменные сохраняли значения после цикла
while IFS= read -r -d '' file; do
    if grep -q "> Если вы обнаружили ошибку в этом тексте - сообщите пожалуйста автору!" "$file"; then
        echo "Пропускаем $file: фраза уже есть."
        ((already_has++))
    else
        # Добавляем перевод строки (если нужно) и саму фразу
        echo >> "$file"
        echo "> Если вы обнаружили ошибку в этом тексте - сообщите пожалуйста автору!" >> "$file"
        echo "✓ Фраза добавлена в $file"
        ((updated++))
    fi
done < <(find . -type f -name "*.md" -print0)

# Проверяем, были ли найдены файлы
if [ $((updated + already_has)) -eq 0 ]; then
    echo "Файлы .md не найдены."
else
    echo ""
    echo "Готово. Обновлено файлов: $updated, пропущено (фраза уже есть): $already_has."
fi
